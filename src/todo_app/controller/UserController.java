package todo_app.controller;

import todo_app.dto.request.UserSignUpRequestDto;
import todo_app.dto.response.UserResponseDto;
import todo_app.entity.User;
import todo_app.service.UserService;
import todo_app.service.implement.UserImpl;

import java.util.List;

public class UserController {
    public UserService service;

    public void registerUser(){
        this.service = new UserImpl();
    }

    public List<UserResponseDto> getAllUsers(){
        List<UserResponseDto> users = service.listAllUsers();
        return users;
    }

    public UserResponseDto getUsernumid(String id, String password){
        UserResponseDto users = service.getUserById(id,password);
        return users;
    }


    public void updateUser(long id, UserSignUpRequestDto dto){
        service.updateUser(id,dto);
    }




}














































































































































































































































































